
export class CommissionMember {
  name:       string;
  desc:       string;
  role:       string;
  emargement: string;

  constructor(name: string, desc: string, role: string, emargement: string) {
    this.name = name;
    this.desc = desc;
    this.role = role;
    this.emargement = emargement;
  }

}
